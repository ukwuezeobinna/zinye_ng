# ── Post-submission operations ─────────────────────────────────────────────────

def confirm_invoice(irn: str) -> dict[str, Any]:
    """
    GET /api/v1/invoice/confirm/{irn}
    Confirms an invoice. Updates local status to Cleared.
    """
    settings = _get_settings()
    resp = requests.get(
        f"{_base_url(settings)}/api/v1/invoice/confirm/{irn}",
        headers=_headers(settings),
        timeout=_TIMEOUT_SHORT,
    )
    if not resp.ok:
        raise EInvoiceError(f"FIRS confirm failed [{resp.status_code}]: {resp.text}")

    result = resp.json() if resp.content else {}

    # Update local e-invoice status to Cleared
    name = frappe.db.get_value("Nigeria E-Invoice", {"irn": irn}, "name")
    if name:
        frappe.db.set_value("Nigeria E-Invoice", name, {
            "status": "Cleared",
            "cleared_at": now_datetime(),
        })
        doc = frappe.get_doc("Nigeria E-Invoice", name)
        frappe.db.set_value("Sales Invoice", doc.sales_invoice, "ng_firs_status", "Cleared")
        frappe.db.commit()
    return result


def update_payment_status(irn: str, payment_status: str, reference: str = "") -> dict[str, Any]:
    """
    PATCH /api/v1/invoice/update/{irn}
    Updates payment status: PENDING | PAID | REJECTED.
    Call when a Sales Invoice is paid/reconciled.
    """
    if payment_status not in ("PENDING", "PAID", "REJECTED"):
        raise EInvoiceError(f"Invalid payment_status: {payment_status}. Must be PENDING, PAID, or REJECTED.")

    settings = _get_settings()
    body: dict = {"payment_status": payment_status}
    if reference:
        body["reference"] = reference

    resp = requests.patch(
        f"{_base_url(settings)}/api/v1/invoice/update/{irn}",
        json=body,
        headers=_headers(settings),
        timeout=_TIMEOUT_SHORT,
    )
    if not resp.ok:
        raise EInvoiceError(f"FIRS update failed [{resp.status_code}]: {resp.text}")
    return resp.json() if resp.content else {}


def download_invoice(irn: str) -> bytes:
    """
    GET /api/v1/invoice/download/{irn}
    Returns raw invoice bytes (XML or PDF depending on Accept header).
    """
    settings = _get_settings()
    headers = dict(_headers(settings))
    resp = requests.get(
        f"{_base_url(settings)}/api/v1/invoice/download/{irn}",
        headers=headers,
        timeout=_TIMEOUT_LONG,
    )
    if not resp.ok:
        raise EInvoiceError(f"FIRS download failed [{resp.status_code}]: {resp.text}")
    return resp.content


def search_invoices(business_id: str, **filters) -> dict[str, Any]:
    """
    GET /api/v1/invoice/{business_id}
    Search invoices with optional filters: irn, payment_status, invoice_type_code,
    issue_date, due_date, tax_currency_code, document_currency_code, size, page, sort_by.
    """
    settings = _get_settings()
    params = {
        "size": filters.get("size", 20),
        "page": filters.get("page", 1),
        "sort_by": filters.get("sort_by", "created_at"),
        "sort_direction_desc": filters.get("sort_direction_desc", True),
    }
    for key in ("irn", "payment_status", "invoice_type_code", "issue_date",
                "due_date", "tax_currency_code", "document_currency_code"):
        if filters.get(key):
            params[key] = filters[key]

    resp = requests.get(
        f"{_base_url(settings)}/api/v1/invoice/{business_id}",
        params=params,
        headers=_headers(settings),
        timeout=_TIMEOUT_SHORT,
    )
    if not resp.ok:
        raise EInvoiceError(f"FIRS search failed [{resp.status_code}]: {resp.text}")
    return resp.json() if resp.content else {}


def cancel_invoice(sales_invoice: str) -> dict[str, Any]:
    """
    Called on Sales Invoice cancellation.
    FIRS has no cancel endpoint — we mark the local record Cancelled only.
    If the invoice was never signed, we can simply cancel locally.
    """
    name = frappe.db.get_value("Nigeria E-Invoice", {"sales_invoice": sales_invoice}, "name")
    if not name:
        return {}

    frappe.db.set_value("Nigeria E-Invoice", name, "status", "Cancelled")
    frappe.db.set_value("Sales Invoice", sales_invoice, "ng_firs_status", "Cancelled")
    frappe.db.commit()
    return {"cancelled": True}


